{
  // 1. 문자열 변수 선언
  let str: string = "Hello";
  console.log(str);

  // 2. 숫자 변수 선언
  let num: number = 42;
  console.log(num);

  // 3. 불리언 변수 선언
  let bool: boolean = true;
  console.log(bool);

  // 4. 숫자 배열 선언
  let arrNum: number[] = [10, 20, 30];
  console.log(arrNum);

  // 5. 문자열 배열 선언
  let arrStr: string[] = ["abc", "def", "ghi"];
  console.log(arrStr);

  // 6. 튜플 변수 선언
  let tuple: [number, string] = [1, "A"];
  console.log(tuple);

  // 7. null 값 할당
  let nulVal: null = null;
  console.log(nulVal);

  // 8. undefined 값 할당
  let udfVal: undefined = undefined;
  console.log(udfVal);

  // 9. 객체 선언
  let obj: {
    name: string;
    age: number;
  } = {
    name: "이재건",
    age: 26,
  };
  console.log(obj);
}
