{
  // 1. 문제: 문자열 변수 선언
  const str: string = "Hello";
  console.log(str);
  // 2. 문제: 숫자 변수 선언
  const num: number = 42;
  console.log(num);
  // 3. 문제: 불리언 변수 선언
  const bool: boolean = true;
  console.log(bool);
  // 4. 문제: 숫자 배열 선언
  const numArr: number[] = [1, 3, 5];
  console.log(numArr);
  // 5. 문제: 문자열 배열 선언
  const strArr: string[] = ["A", "B", "C"];
  console.log(strArr);
  // 6. 문제: 튜플 변수 선언
  const tup: [number, string] = [1, "A"];
  console.log(tup);
  // 7. 문제: null 값 할당
  const nul: null = null;
  console.log(nul);
  // 8. 문제: undefined 값 할당
  const und: undefined = undefined;
  console.log(und);
  // 9. 문제: 객체 선언
  const obj: {
    name: string;
    age: number;
  } = {
    name: "Alice",
    age: 25,
  };
  console.log(obj);
}
