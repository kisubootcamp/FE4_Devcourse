// 0부터 15까지 출력하면서 각각의 수가 짝수인지 홀수인지 구분하여 출력하도록 코드를 작성해주세요.
// 출력 예시 ) 
// 0 짝수 
// 1 홀수
// 2 짝수
// ... 중략 .. 
// 15 홀수
{
    for (let i = 0; i <= 15; i++) {
        if (i % 2 === 0) {
            console.log(`${i} 짝수`);
        }
        else {
            console.log(`${i} 홀수`);
        }
    }
}